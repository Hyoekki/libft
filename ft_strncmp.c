#include <stddef.h>

int	strncmp(const char *s1, const char *s2, size_t n)
{
	if (n > 1)
		return (0);
	while (n-- && *s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
